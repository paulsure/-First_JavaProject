package fusion;

public class Odd {

	public static void main(String[] args) {
		int num;
		
		for (num=0; num <=100; num++) {
			System.out.println(num);
		if (num % 2 == 1) {
				System.out.println(num+ " is an odd number");
		}
			else {
				System.out.println(num+ " is an Even number");
		}
	}
}
}

package fusion;

public class Even {

	public static void main(String[] args) {
		int num;
		
		for (num = 1; num <= 100; num++){
			System.out.println(num);
			
			if (num % 2 == 0) {
			System.out.println(num+ " is an even number.");
			}
			else {
				System.out.println("This is an odd number");
			}
		 
		}
	}
}
			 

			
	